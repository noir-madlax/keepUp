def call_openrouter(prompt, content):
    api_key = settings.OPENROUTER_API_KEY
    url = "https://openrouter.ai/api/v1/chat/completions"
    
    headers = {
        "Authorization": f"Bearer {api_key}",
        "Content-Type": "application/json",
        "HTTP-Referer": "https://keep-up.vercel.app", 
        "X-Title": "Keep Up"
    }
    
    data = {
        "model": MODEL_ID,
        "messages": [
            {"role": "system", "content": prompt},
            {"role": "user", "content": content}
        ],
        "temperature": 0.1
    }
    
    # Save input
    output_dir = os.path.abspath(os.path.join(os.path.dirname(__file__), 'output'))
    os.makedirs(output_dir, exist_ok=True)
    
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    with open(os.path.join(output_dir, f'input_{timestamp}.json'), 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
        
    print(f"Calling OpenRouter with model: {MODEL_ID}...")
    response = requests.post(url, headers=headers, json=data, timeout=180)
    
    if response.status_code != 200:
        print(f"Error calling API: {response.text}")
        return None
        
    result = response.json()
    
    # Save output
    with open(os.path.join(output_dir, f'output_{timestamp}.json'), 'w', encoding='utf-8') as f:
        json.dump(result, f, ensure_ascii=False, indent=2)
        
    if 'choices' in result and len(result['choices']) > 0:
        return result['choices'][0]['message']['content']
    
    return None

